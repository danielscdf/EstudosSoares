package br.jus.educacional.aula06;

public class ArvoreB {

	private int raiz;
	private int noDireito;
	private int noEsquerdo;

	public void setRaiz(int raiz) {
		this.raiz = raiz;
	}

	public void setNoDireito(int noDireito) {
		this.noDireito = noDireito;
	}

	public void setNoEsquerdo(int noEsquerdo) {
		this.noEsquerdo = noEsquerdo;
	}

	public int getRaiz() {
		return raiz;
	}

	public int getNoDireito() {
		return noDireito;
	}

	public int getNoEsquerdo() {
		return noEsquerdo;
	}
}
